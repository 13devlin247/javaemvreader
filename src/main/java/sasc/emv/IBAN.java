package sasc.emv;

/**
 * International Bank Account Number (IBAN)
 * Uniquely identifies the account of a customer at a financial institution as defined in ISO 13616
 *
 * Length 0-34 bytes
 *
 * TODO
 *
 * @author sasc
 */
public class IBAN {
//IBAN - International Bank Account Number. Often (incorrectly) referred to as IBAN Number. 
//An unique account identifier for every bank account in Europe, supervised by SWIFT. 
//The IBAN system was originally intended for use within Europe, and its use is 
//largely confined thereto: most notably, the USA does not participate. 
//Among British dependencies, only Gibraltar assigns IBANs. 
//All of Europe does however participate with the exception of the CIS countries: 
//urkey uses IBANS and so do some African countries. The number consists first of a 
//two-digit country identifier, followed by two check digits, then up to 30 characters representing the local account. 
//The number of characters varies from country to country, but is fixed for each country. 
//When used electronically there are no spaces in IBANs, but the convention on paper 
//is to separate into groups of four with any odd digits in the last group.
}
